%
%  See also FIT, CFIT, SFIT.

%  Auto-generated by MATLAB on 09-Jan-2012 13:43:12

warning off;
%% Fit: 'untitled fit 1'.


[xData, yData] = prepareCurveData( t, y );

% Set up fittype and options.
ft = fittype( '(a*exp(-b*(x))+c)/(1+exp(d-x)^2)+e/(1+exp(x-d)^2)', 'independent', 'x', 'dependent', 'y' );
opts = fitoptions( ft );
opts.Display = 'Off';
opts.Lower =minLimit;
opts.Robust = 'LAR';
opts.StartPoint = startPoint;
opts.Upper =maxLimit;

% Fit model to data.
[fitresult, gof] = fit( xData, yData, ft, opts );

% Plot fit with data.
if(graph)
    figure(2);
    h = plot( fitresult, xData, yData );
    grid off
    legend('off')
    warning on;
end